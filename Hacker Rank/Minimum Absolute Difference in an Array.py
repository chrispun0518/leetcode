# Complete the minimumAbsoluteDifference function below.
def minimumAbsoluteDifference(arr):
    mad = []
    for index, v1 in enumerate(arr):
        for v2 in arr[index+1:]:
            mad.append(abs(v1 - v2))
    return min(mad)
